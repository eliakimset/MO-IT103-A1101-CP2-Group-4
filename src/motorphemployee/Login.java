    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
    String employeeNumber = txtemployeenumber.getText();
    String username = txtusername.getText();
    String password = new String(txtpassword.getPassword());

    if (employeeNumber.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter your employee number");
        return;
    } 
    if (username.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter your username");
        return;
    } 
    if (password.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter your password");
        return;
    }

    if (validateCredentials(employeeNumber, username, password)) {
        JOptionPane.showMessageDialog(null, "Log in successful");
        // Open the Dashboard
        Dashboard dashboard = new Dashboard(employeeNumber);
        dashboard.setVisible(true);
        // Close the current login window
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(null, "Incorrect employee number, username, or password!", "Message", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_loginActionPerformed

