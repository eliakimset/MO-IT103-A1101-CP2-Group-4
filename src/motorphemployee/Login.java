    private boolean validateCredentials(String employeeNumber, String username, String password) {
    try (BufferedReader reader = new BufferedReader(new FileReader("src/motorphemployee/data.csv"))) {
        String line;
        while ((line = reader.readLine()) != null) {
            String[] parts = line.split(",");
            if (parts.length >= 3 && parts[0].equals(employeeNumber) && parts[19].equals(username) && parts[20].equals(password)) {
                // Credentials match
                return true;
            }
        }
    } catch (IOException ex) {
        JOptionPane.showMessageDialog(null, "Error occurred while logging in. Please try again later.");
        ex.printStackTrace();
    }
    return false;
}
